package day2;

public class Ifelse {

	public static void main(String[] args) {
		 int a = 10;
	        int b = 20;

	        if (a <= b)
	        {
	            System.out.println(a + " is less than or equals to " + b);
	        }
	        else
	        {
	            System.out.println(a + " is greater than " + b);
	        }
	    }
	}
