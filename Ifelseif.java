package day2;

public class Ifelseif {

	public static void main(String[] args) {
		int a = 5;
        int b = 10;

        if (a < b)
        {
            System.out.println("a is less than b");
        }
        else if (a > b)
        {
            System.out.println("a is greater than b");
        }
        else
        {
            System.out.println("Both are equal");
        }
    }

}